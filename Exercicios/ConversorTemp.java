// Declaracao da classe;
public class ConversorTemp{

    // Metodo principal;
    public static void main(String[] args){

        // Operacoes matematicas;
        float grausc = (float) 32.5;
        float grausf = ((9 * grausc) / 5) + 32;

        // Impressao dos resultados;
        System.out.println("A temperatura de "+grausc+" graus celsos, corresponde a "+grausf+" graus faranheits") ;
    }
}